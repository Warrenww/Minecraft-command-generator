$(document).ready(function(){

  $('select:not([data-dismisschange])').change(function(){
    $(this).parents(".content").find(".hide").hide();
    $(this).parents(".content").find("."+$(this).val()).show();
  });

  $(".content").each(function(){
    if(!$(this).attr("id")) return;
    $("#tableOfContent ul").append(`<li><a href='#${$(this).attr("id")}'>${$(this).find('h1').text()}</a></li>`)
  });

  for(let catagory in AllBlocks){
    let optgroup = $("<optgroup label='"+catagory+"'>");
    for(let id in AllBlocks[catagory]){
      optgroup.append("<option value='"+id+"'>"+AllBlocks[catagory][id]+"</option>");
    }
    $(".block_selector").append(optgroup);
  }

  for(let catagory in AllEntities){
    let optgroup = $("<optgroup label='"+catagory+"'>");
    for(let id in AllEntities[catagory]){
      optgroup.append("<option value='"+id+"'>"+AllEntities[catagory][id]+"</option>");
    }
    $(".entity_selector").append(optgroup);
  }

  for(let catagory in AllItems){
    let optgroup = $("<optgroup label='"+catagory+"'>");
    for(let id in AllItems[catagory]){
      optgroup.append("<option value='"+id+"'>"+AllItems[catagory][id]+"</option>");
    }
    $(".item_selector").append(optgroup);
  }

  $("#tableOfContent .trigger").click(() => {
    if($("#tableOfContent").is(".active")) $("#tableOfContent").removeClass("active");
    else $("#tableOfContent").addClass('active');
  });

  $("form.content").submit(e => {
    e.stopPropagation();
    e.preventDefault();

    let result = "";
    if($(e.target).is("#selector")){
      $(".constrain").each(function(){
        let tmp = $(this).children();
        if(!tmp.eq(0).prop("checked")) return;
        tmp.each(function(){
          if($(this).prop("checked")) return;
          result += ($(this).val() || $(this).attr('data-key'));
        });
        result += ',';
      });
      result = result.split(",");
      result.pop();
      result = result.join(",");
      $("#result").val($("#selector_base").val() + `[${result}]`);
    }
    else if($(e.target).is("#manifest")){
      let obj = {
        format_version: 1,
        header:{
          description:  $(e.target).find("input").eq(1).val(),
          name: $(e.target).find("input").eq(0).val(),
          uuid: _uuid(),
          version: [Number($(e.target).find("input").eq(2).val()), Number($(e.target).find("input").eq(3).val()), Number($(e.target).find("input").eq(4).val())],
          min_engine_version: [Number($(e.target).find("input").eq(5).val()), Number($(e.target).find("input").eq(6).val()), Number($(e.target).find("input").eq(7).val())],
        },
        modules:[
            {
            description:  $(e.target).find("input").eq(1).val(),
            type: "resources",
            uuid: _uuid(),
            version: [Number($(e.target).find("input").eq(2).val()), Number($(e.target).find("input").eq(3).val()), Number($(e.target).find("input").eq(4).val())],
          }
        ]
      }
      var blob = new Blob([JSON.stringify(obj, null, 2)], {type : 'application/json'});
      var url = URL.createObjectURL(blob);
      var a = $("<a href='"+url+"' download='manifest.json' style='color:#4ea9c3;font-weight:bold'>點此下載</a>");
      $(e.target).find('a').remove();
      $(e.target).append(a);
    }
    else {
      let target = $(e.target);
      result += '/'+target.attr('commad')+' ';
      target.find('select:visible, input:visible').each(function(){
        if($(this).is(".ignore")) return;
        result += $(this).val() + ' ';
      });
      $("#result").val(result);
    }
    return false;
  });

});
